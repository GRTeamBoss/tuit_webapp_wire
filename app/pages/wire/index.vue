      <b> Телефонные кабели и провода </b>

      <p>
        Телефонные провода делятся на 2 основных вида. Первые предназначены для
        прокладки нескольких (до 400) абонентских линий. Второй вид применяется
        для разводки в отдельно взятых квартире или доме.
      </p>
      <img
        src="https://540007.ru/image/catalog/articles/266.jpg"
        alt="ТППэп: 1 — жила; 2 — полиэтиленовая изоляция; 3 — сердечник; 4 — скрепляющая обмотка; 5 — поясная изоляция; 6 — экран"
      />

      <p>
        ТППэп — основной вид кабеля для прокладки линий телефонной связи,
        рассчитанной на большое количество абонентов. Кабель состоит из двойки
        проводов, свитых в пары. ТПЖ из мягкой медной проволоки, сечением 0,4
        или 0,5 мм², покрыта полиэтиленовой изоляцией. В некоторых видах кабеля
        пары объединены в группы по 5 или 10 пар. Внешняя оболочка также
        полиэтиленовая или виниловая. Буквы «э» и «п» в названии обозначают
        пленочный экран.
      </p>

      <p>
        Встречаются разновидности кабеля, бронированного лентами, или
        заполненного, в котором пространство между оболочкой и жилами занимает
        гидрофобный уплотнитель. Словом, это кабель для проведения телефонной
        связи в многоквартирный дом, предназначен он для прокладки практически
        во всех условиях: под землей, в кабельных каналах или воздушным путем.
        Для проведения телефонной линии к отдельному абоненту и разводки внутри
        помещения используются телефонные провода следующих видов.
      </p>

      <img
        src="https://540007.ru/image/catalog/articles/262.jpg"
        alt="Телефонный провод ТРВ"
      />

      <p>
        ТРВ — одно- или двупарный телефонный распределительный провод. Это
        плоский провод с разделенным основанием, жила медная, однопроволочная,
        сечением 0,4 или 0,5 мм². Количество жил — 2 или 4. Изоляция из ПВХ.
        Предназначен для проведения телефонных линий внутри помещений.
        Эксплуатируется при температуре от –10 до +40 °C. Влажность не должна
        превышать 80 % при температуре +30 °C.
      </p>

      <img
        src="https://540007.ru/image/catalog/articles/271.jpg"
        alt="Провод ТРП"
      />

      <p>
        ТРП — по характеристикам совпадает с ТРВ. Единственное отличие — это
        изоляция, у ТРП она сделана из полиэтилена. По сравнению с ТРВ провод
        более устойчив к воздействию внешней среды и его можно прокладывать
        снаружи зданий.
      </p>

      <img
        src="https://540007.ru/image/catalog/articles/281.jpg"
        alt="Провод ШТЛП"
      />

      <p>
        ШТЛП — телефонный плоский шнур с медными многопроволочными жилами.
        Изоляция жил из полиэтилена. Изолированные ТПЖ покрыты оболочкой из ПВХ.
        Количество жил — 2 или 4, сечение — от 0,08 до 0,12 мм². Используется
        для проведения линий внутри помещений и в телефонных аппаратах. Провод
        повышенной гибкости.
      </p>
      <img
        src="https://540007.ru/image/catalog/articles/292.jpg"
        alt="Провод ПРППМ"
      />

