        вибрации, повышенной влажности (до 100 % при температуре +35 °C),
        термостоек (диапазон эксплуатационных температур — от –60 до +180 °C).
        Кроме того, провод защищен от вредного воздействия лаков, растворителей
        и грибковой плесени. Идеальный проводник для помещений с повышенной
        температурой (котельные и печи), подходит для электромонтажа в банях,
        саунах, подключений духовых шкафов.
