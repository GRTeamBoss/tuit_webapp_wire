      <p>
        Светодиодный кабель — очень интересный вариант силового. Под прозрачной
        внешней оболочкой вдоль силовых ТПЖ размещены дополнительные провода с
        последовательно подсоединенными светодиодами разного цвета. Они
        расположены на расстоянии 2 см друг от друга, горят постоянным
        достаточно сильным светом. Такой кабель выполняет не только декоративные
