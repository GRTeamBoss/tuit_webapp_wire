        изоляцию из полиэтилена, оболочка также из полиэтилена или ПВХ. Сечение
        жилы — от 1,2 до 25 мм². Номинальное напряжение — 380 или 660 В, частота
        — 50 Гц. Провод устойчив к перемене давления. Диапазон рабочих
        температур — от –40 до +80 °C. Применяется для двигателей артезианских
        скважин, погруженных в воду в условиях высокого давления.
      </p>

