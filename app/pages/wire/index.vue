        S/FTP, или фольгированная, экранированная с общим экраном из фольги, к
        тому же каждая пара дополнительно заключена в экран.
      </p>
      <img
        src="https://540007.ru/image/catalog/articles/251.jpg"
        alt="Наконечник RJ-45 для подсоединения к компьютеру"
      />
      <p>
        Кроме того, витые пары разделяются на категории по количеству пар,
        объединенных в один кабель. Самый распространенный вид, применяемый для
        компьютерных сетей, — это категория CAT5e. Он состоит из 4 пар проводов
        различного цвета. Скорость передачи данных — до 1 Гб/с при использовании
        всех пар. Можно увидеть такой кабель, использующийся в качестве
        телефонного провода категории CAT1 или САТ2, то есть состоящий из 1 или
        2 пар проводов.
      </p>
      <img
        src="https://540007.ru/image/catalog/articles/232.jpg"
        alt="Витая пара с коннектором для подсоединения к компьютеру, защищенная оболочкой из ПВХ"
