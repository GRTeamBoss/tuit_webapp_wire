        ПРППМ — плоский провод с разделяющим основанием и медными
        однопроволочными жилами с изоляцией и оболочкой из полиэтилена.
        Существует модификация ПРПВМ, оболочка которого изготовлена из ПВХ.
        Количество жил — 2, сечение жилы — 0,9 или 1,2 мм². Применяется при
        прокладке телефонной линии вне помещения, на воздушных опорах, в земле и
        по стенам зданий. Стоек к температурному воздействию, условия
        эксплуатации — от –60 до +60 °C.
      </p>

      <b> Специальные виды кабелей и проводов </b>

      <p>
        Для монтажа электрических систем в местах, условия которых сильно
        отличаются от обычных, используются специальные кабели, обладающие
        повышенной устойчивостью к воздействию внешней среды. К таким местам
        относятся бани, печи и погреба. В общем, везде, где слишком жарко,
        влажно или холодно и к тому же есть вероятность механического
        повреждения. Понятно, что ПВС или ВВГ в таких местах устанавливать
        нельзя, не говоря уже о ПУНП или ШВВП.
      </p>

      <img
        src="https://540007.ru/image/catalog/articles/302.jpg"
        alt="Провод РКГМ"
